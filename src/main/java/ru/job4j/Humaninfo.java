package ru.job4j;

public class Humaninfo {
    public static void main(String[] args) {
        System.out.println("---Информация---");
        byte age = 19;
        System.out.println("Возвраст: " + age);
        char sex = 'M';
        System.out.println("Пол: " + sex);
        float height = 1.83f;
        System.out.println("Рост: " + height);
        int bricks = 1000000;
        System.out.println("Количество кирпичей: " + bricks);
        double bacteria = 100000000000L;
        System.out.println("КОЕ/г: " + bacteria);
        double lightyear = 10000000000000000L;
        System.out.println("Сколько километров в 1057 св.г. : " + lightyear);
        boolean work = true;
        System.out.println("Разрешение на работу: " + work);
        long bytes = 1000000000000000000L;
        System.out.println("Размер файла (bytes): " + bytes);
    }
}
